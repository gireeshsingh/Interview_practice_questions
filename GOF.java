	public int[][] computeLife(int arr[][]){
		int i=0,j=0;
		int total=0;
		int newArr[][]= new int[rows][cols];
		for(i=0;i<rows;i++){
			for(j=0;j<cols;j++){
				total=0;
				if(i>0){
					total=total+arr[i-1][j];
				}
				if(i<(rows-1)){
					total=total+arr[i+1][j];
				}
				if((j+1)<cols){
					total=total+arr[i][j+1];
					if((i+1)<rows){
						total=total+arr[i+1][j+1];
					}
					if((i-1)>=0){
						total=total+arr[i-1][j+1];
					}
				}
				if(j>0){
					total=total+arr[i][j-1];
					if((i+1)<rows){
						total=total+arr[i+1][j-1];
					}
					if((i-1)>=0){
						total=total+arr[i-1][j-1];
					}
				}
				
				if( arr[i][j]==1 ){
					if(total<2 || total>3){
						newArr[i][j]=0;
					}
					else{
						newArr[i][j]=1;
					}
				}
				else if(arr[i][j]==0){
					if(total ==3){
						newArr[i][j]=1;
					}
					else{
						newArr[i][j]=0;
					}
				}
				
			}
		}
		
		return newArr;
	}
	
